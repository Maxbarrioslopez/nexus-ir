export type Lang = "es" | "en"

const translations: Record<Lang, DeepRecord> = { es, en }

export function t(key: string, lang: Lang): string {
  const keys = key.split(".")
  let current: DeepRecord | string | undefined = translations[lang]

  for (const k of keys) {
    if (typeof current === "object" && current !== null && k in current) {
      current = current[k] as DeepRecord | string
    } else {
      return key
    }
  }

  if (typeof current === "string") return current

  const fallback = translations["es"]
  let fallbackCurrent: DeepRecord | string | undefined = fallback
  for (const k of keys) {
    if (typeof fallbackCurrent === "object" && fallbackCurrent !== null && k in fallbackCurrent) {
      fallbackCurrent = fallbackCurrent[k] as DeepRecord | string
    } else {
      return key
    }
  }

  return typeof fallbackCurrent === "string" ? fallbackCurrent : key
}

export function availableLangs(): Lang[] {
  return ["es", "en"]
}

export function langLabel(lang: Lang): string {
  return lang === "es" ? "Español" : "English"
}
